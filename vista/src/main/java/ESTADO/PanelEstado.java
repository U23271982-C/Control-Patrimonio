    private void cargarEstados(List<EstadoDTO> lista) {
        this.listaestado = lista;
    DefaultTableModel modelo = new DefaultTableModel(
    new String[]{"Estado"}, 0
    );
    
    for (EstadoDTO estado : lista) {
        modelo.addRow(new Object[]{
            estado.getTipo(),
        });
    }
    tabla_estados.setModel(modelo);
    }
