    public void recargarTablaRoles() {
    List<Rol_ResponsableDTO> lista = controlador.listarTodo();
    DefaultTableModel modelo = new DefaultTableModel(
        new String[]{"Nombre", "Descripción"}, 0
    );
    for (Rol_ResponsableDTO r : lista) {
        modelo.addRow(new Object[]{ r.getNombreRol(), r.getDescripcion() });
    }
    Tabla_ListaRolResponsables.setModel(modelo);
}
